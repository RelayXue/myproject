package com.gh.entity;

/**
 * @ClassName AutoCompleteEntity 
 * @Description  自动补全实体类
 * @author ieastar
 * @date 2014-12-12
 * @version V1.0
 */
public class AutoCompleteEntity {
	/**
	 * 名称
	 */
	private String name;
	
	/**
	 * x坐标
	 */
	private String x;
	
	/**
	 * y坐标
	 */
	private String y;
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}
}
